    	<h3>DOS SERVIÇOS A SEREM EXECUTADOS (PRODUTOS)</h3>
        <p class="vaga">
        	No Ambiente Virtual de Aprendizagem (AVA) são disponibilizados os conteúdos das disciplinas dos cursos, organizados por roteiros de aprendizagem (conjunto de recursos educacionais organizados  de forma a atingir determinado objetivo pedagógico ou a desenvolver certa competência, no espaço de uma semana) e disponibilizadas às quartas-feiras aos estudantes. Os roteiros são apresentados de forma organizada aos estudantes, contendo links para leituras de textos em bibliotecas virtuais, artigos, vídeos, recursos educacionais abertos e outros materiais didáticos elaborados por docentes autores em colaboração com a equipe da universidade. Após percorrer o roteiro de aprendizagem, cada estudante deve realizar, periodicamente, uma atividade avaliativa, que poderá ser entregue por meio de: envio de arquivo, fórum, testes ou outras ferramentas disponíveis no AVA.</p>

            <p class="vaga">Para cada disciplina devem ser produzidas videoaulas em quantitativo condizente com a  carga horária da disciplina. As videoaulas são apresentadas como elementos para auxiliar os estudantes na aprendizagem ativa e na resolução de problemas ou nas atividades avaliativas.</p>

            <p class="vaga">Os responsáveis pela organização dos conteúdos das disciplinas devem ser liderados pelo Coordenador de Curso, que organizará atividades de projetos que integrem os conhecimentos adquiridos em todas as disciplinas do período, eventualmente, até com conhecimentos de períodos anteriores.</p>

			<p class="vaga">Com a estrutura didático-metodológica descrita anteriormente, faz-se importante definir a organização e as atribuições dos especialistas que desenvolvem os roteiros de aprendizagem para que o estudante tenha as melhores condições possíveis de aprender.</p>

			<p class="vaga">Entre outras funções descritas brevemente ao fim deste tópico, os especialistas prestam serviços de Autoria (que envolve produção e gravação de videoaulas), serviços de Revisão de Conteúdo e serviços de Acompanhamento da oferta:</p>

			<p class="vaga">Serviços de Autoria (aplicam-se a disciplinas em primeira oferta): participar da reunião inicial de apresentação do modelo pedagógico da Univesp e do modelo específico de disciplina, que orientará a autoria dos roteiros de aprendizagem; elaborar o plano de ensino da disciplina, seguindo os critérios:</p>

			<p class="vaga">da instituição e do Projeto Pedagógico do Curso; preparar e gravar videoaulas, de acordo com quantitativo e instruções da equipe de produção de material didático da Univesp, seguindo a carga horária da disciplina; elaborar atividades para avaliação processual no AVA, incluindo produção de questões para a prova regular e para o exame da disciplina, de acordo com instruções e quantitativo indicados pela equipe de produção de material didático; organizar os roteiros de aprendizagem para a disciplina, em um número de 7 ou 8 (de acordo com o modelo de disciplina do curso), contendo introdução, objetivos de aprendizagem, indicação de conteúdos como vídeos, artigos, capítulos de livros etc., atividades avaliativas, fóruns e síntese do roteiro; conferir, corrigir e/ou validar os roteiros/storyboards propostos pela equipe de produção de material didático da Univesp, bem como os produtos gerados a partir destes roteiros/storyboards (como recursos interativos, games, videoaulas etc.); validar o roteiro montado no AVA antes da disponibilização aos alunos; criar provas segundo o quantitativo e os critérios indicados pela equipe de produção de material didático da Univesp; seguir rigorosamente os prazos e as orientações da equipe de produção de material didático da Univesp; participar da criação das orientações e do suporte teórico ao Projeto Integrador da turma, com apoio dos demais profissionais da instituição.</p>

			<p class="vaga">Serviços de Revisão de Conteúdo (aplicam-se a disciplinas em reoferta): participar da reunião inicial de apresentação do modelo pedagógico da Univesp e do modelo específico de disciplina, que orientará a revisão dos materiais; elaborar atividades para avaliação processual no AVA, de acordo com instruções e quantitativo indicados pela equipe de produção de material didático; revisar os roteiros de aprendizagem já existentes para a disciplina, em um número de 7 ou 8 (de acordo com o modelo de disciplina do curso), contendo introdução, objetivos de aprendizagem, indicação de conteúdos como vídeos, artigos, capítulos de livros etc., atividades avaliativas, fóruns e síntese do roteiro, sugerindo melhorias, caso ocorra desatualização/erro de informação, a avaliação da disciplina tenha sido insatisfatória ou os materiais não favoreçam mais os objetivos pedagógicos; validar o roteiro antes da disponibilização aos alunos; criar provas segundo o quantitativo e os critérios indicados pela equipe de produção de material didático da Univesp; seguir rigorosamente os prazos e as orientações da equipe de produção de material didático da Univesp; participar da criação das orientações e do suporte teórico ao Projeto Integrador da turma, com apoio dos demais profissionais da instituição.</p>

			<p class="vaga">Serviços de Acompanhamento de oferta (aplicam-se a todas as disciplinas): acompanhar no AVA, durante o bimestre letivo, a oferta dos conteúdos e das atividades; dirimir eventuais dúvidas dos tutores, mediadores e supervisores sobre o conteúdo; como última instância, dirimir eventuais dúvidas dos alunos que os tutores e mediadores não consigam resolver; validar e/ou resolver eventuais dúvidas sobre a produção de conteúdo, inclusive aquelas a respeito das provas presenciais; supervisionar a aprendizagem dos alunos, sugerindo possíveis ações preventivas ou corretivas nos trabalhos à Coordenação do Curso; conceber atividades avaliativas e/ou provas e seus respectivos gabaritos para casos em que seja necessária uma nova aplicação (alunos afastados legalmente ou matriculados tardiamente); participar do acompanhamento do Projeto Integrador da etapa; produzir o material de revisão da disciplina (webconferência ao vivo, vídeo de revisão gravado ou texto derevisão), seguindo prazos e orientações da equipe de produção de material didático e de forma a responder às dúvidas dos alunos, coletadas por meios específicos.</p>

            <p class="vaga">Adicionam-se à estrutura didático-metodológica as funções de Coordenação Geral dos Cursos, Coordenação de cada Curso, Supervisores e Mediadores Presenciais, além da equipe multidisciplinar da UNIVESP. Essas funções não serão objeto deste Termo de Referência.</p>

            <p class="vaga">Deve ser observado pelos prestadores de serviço, tendo como hipótese a disciplina específica atribuída à prestação de serviços, conforme segue:</p>

            <p class="vaga">Os serviços de autoria devem ser aplicados às disciplinas que são ofertadas pela primeira vez ou àquelas que, após a análise do coordenador de curso, necessitam de reformulação completa em seu plano de ensino.</p>

            <p class="vaga">Os serviços de revisão de conteúdo devem ser aplicados às disciplinas já produzidas e  que serão ofertadas para novas turmas.</p>

            <p class="vaga">Os serviços de acompanhamento de oferta devem ser aplicados a todas as disciplinas.</p>

